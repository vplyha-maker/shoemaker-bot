    text = (
        "🎨 **Раздел «Цвета и Колористика»**\n\nВыберите подраздел:"
        if lang == "ru" else
        "🎨 **Розділ «Кольори та Колористика»**\n\nОберіть підрозділ:"
    )
    await safe_edit_or_send(callback, text, get_colors_main_menu_keyboard(lang))



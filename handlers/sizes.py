async def safe_edit_or_send(callback: types.CallbackQuery, text: str, reply_markup=None):
    try:
        await callback.message.edit_text(text, reply_markup=reply_markup, parse_mode="Markdown")
    except TelegramBadRequest:
        await callback.message.delete()
        await callback.message.answer(text, reply_markup=reply_markup, parse_mode="Markdown")


@router.callback_query(F.data == "menu_sizes")
async def show_sizes_root(callback: types.CallbackQuery):
    """Корневое меню раздела Размеры"""
    await callback.answer()
    lang = user_language.get(callback.from_user.id, "ru")
    text = SIZES_WELCOME.get(lang, SIZES_WELCOME["ru"])
    
    await safe_edit_or_send(callback, text, get_sizes_menu_keyboard(lang))


@router.callback_query(F.data.startswith("sizes_"))
async def show_sizes_detail(callback: types.CallbackQuery):
    """Отображение конкретной статьи из раздела Размеры"""
    await callback.answer()
    lang = user_language.get(callback.from_user.id, "ru")
    category = callback.data.replace("sizes_", "")
    
    text_data = SIZES_TEXTS.get(category, {}).get(lang, "Контент не найден." if lang == "ru" else "Контент не знайдено.")
    
    await safe_edit_or_send(callback, text_data, get_sizes_detail_keyboard(lang))


def register_sizes_handlers(dp):
    dp.include_router(router)

